#include <bits/stdc++.h>

using namespace std;

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    vector<int> v;
    int input;
    
    for(int i = 0; i < 5; i++) {
		cin >> input;
		v.push_back(input);
	}
	
	//cout << v[3] * 2 + v[0] * v[1] << " " << v[4] * 2 + v[2] * v[0];
    if(v[3] * 2 + v[0] * v[1] == v[4] * 2 + v[2] * v[0]) {
		cout << "Friendship";
	} else if(v[3] * 2 + v[0] * v[1] < v[4] * 2 + v[2] * v[0]) {
		cout << "First";
	} else 
		cout << "Second";
}
